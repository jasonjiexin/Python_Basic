'''
格式化字符串
格式化标识符格式化字符串
类C语言(print 函数)
'''
#3 表示有效数字是3
print("i am %3d" % (20,))
print("i am %3d" % (2000))
print("i am %d" % (20))
# 0表示不够三位的元素用0补充位置
print("i am %03d" % (20))
# print("i am %03d" % (20, 21)) #占位符与后边括号中元组的元素个数一致，有几个占位符就有几个元素参数

# 打印百分号
print("i am %s %%" % (20))

print("%3.2f%%, 0x%x, 0X%02X" % (89.7654, 10,15))  # x打印十六进制

# 要求打印两位有效数字，但是已经超出去了，不能进行截取，必须全打印
print("i am %2d %%" % (8923))

# 打印5位有效位数,默认都是右对齐
print("i am %5d" % (200))
# 打印5位有限位数并且左对齐
print("i am %-5d" % (200))